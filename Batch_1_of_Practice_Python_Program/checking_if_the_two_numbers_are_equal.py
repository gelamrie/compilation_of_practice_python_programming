#Ask the user to enter two numbers to be stored in num_1 and num_2
num_1 = int(input("Enter the first number: "))
num_2 = int(input("Enter the second number: "))

#Checking if the two numbers are equal
if num_1 == num_2:
    print("The two numbers are equal.") #Print "equal" if the numbers are equal
else: 
    print("The two numbers are not equal.") #Print "not equal" if the numbers are not equal